#Oppgave 2: Variabler

#1.
#Setter to variabler til verdiene 2 og 5.
heltallsverdi_1= 2
heltallsverdi_2= 5

#Jeg printer begge variablene på hver sin linje.
print(heltallsverdi_1)
print(heltallsverdi_2)

#2.
#Jeg lager en ny variabel heltallsverdi_3 og setter den til å være
#heltallsverdi_1 summert med heltallsverdi_2. Deretter printer jeg
#tekstrengen "Sum:" og det som ligger i heltallsverdi_3.
heltallsverdi_3= heltallsverdi_1 + heltallsverdi_2
print("Sum:", heltallsverdi_3)

#3.
#Jeg lager to nye variabeler og fyller dem med selvvalgt tekst.
#Begge variablene printes på hver sin linje. Jeg lager en ny
#variabel sammen og summerer tekstvariabel_1 og tekstvariabel_2.
#Variabelen sammen printes.
tekstvariabel_1= "Hei på deg"
tekstvariabel_2= "her er jeg!"
print(tekstvariabel_1)
print(tekstvariabel_2)
sammen= tekstvariabel_1 + tekstvariabel_2
print(sammen)

#4.
#Variabelen sammen endres ved å legge til et mellomrom mellom
#tekstvariablene. Variabelen sammen printes.
sammen=tekstvariabel_1 + " " + tekstvariabel_2
print(sammen)
